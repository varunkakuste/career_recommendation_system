package com.sjsu.edu.beans;

public class DashBoardBean {
	
	private int noOfRecommendedJobs;
	private int noOfRecommendedSkills;
	private int noOfSavedJobs;
	private int noOfAppliedJobs;
	private boolean isFirstTime;
	
	/**
	 * @return the noOfRecommendedJobs
	 */
	public int getNoOfRecommendedJobs() {
		return noOfRecommendedJobs;
	}
	/**
	 * @param noOfRecommendedJobs the noOfRecommendedJobs to set
	 */
	public void setNoOfRecommendedJobs(int noOfRecommendedJobs) {
		this.noOfRecommendedJobs = noOfRecommendedJobs;
	}
	/**
	 * @return the noOfRecommendedSkills
	 */
	public int getNoOfRecommendedSkills() {
		return noOfRecommendedSkills;
	}
	/**
	 * @param noOfRecommendedSkills the noOfRecommendedSkills to set
	 */
	public void setNoOfRecommendedSkills(int noOfRecommendedSkills) {
		this.noOfRecommendedSkills = noOfRecommendedSkills;
	}
	/**
	 * @return the noOfSavedJobs
	 */
	public int getNoOfSavedJobs() {
		return noOfSavedJobs;
	}
	/**
	 * @param noOfSavedJobs the noOfSavedJobs to set
	 */
	public void setNoOfSavedJobs(int noOfSavedJobs) {
		this.noOfSavedJobs = noOfSavedJobs;
	}
	/**
	 * @return the noOfAppliedJobs
	 */
	public int getNoOfAppliedJobs() {
		return noOfAppliedJobs;
	}
	/**
	 * @param noOfAppliedJobs the noOfAppliedJobs to set
	 */
	public void setNoOfAppliedJobs(int noOfAppliedJobs) {
		this.noOfAppliedJobs = noOfAppliedJobs;
	}
	/**
	 * @return the isFirstTime
	 */
	public boolean isFirstTime() {
		return isFirstTime;
	}
	/**
	 * @param isFirstTime the isFirstTime to set
	 */
	public void setFirstTime(boolean isFirstTime) {
		this.isFirstTime = isFirstTime;
	}
}
